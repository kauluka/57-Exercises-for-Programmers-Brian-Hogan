# 5. Simple Math
num_1 = input("What is the first number? ")
num_1 = int(num_1)

num_2 = input("What is the second number? ")
num_2 = int(num_2)

print(num_1, " + " , num_2 , " = " , (num_1+num_2))
print(num_1, " - ", num_2, " = " , (num_1-num_2))
print(num_1, " * ", num_2, " = " , (num_1*num_2))
print(num_1, " / ", num_2, " = " , (num_1/num_2))
